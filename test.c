void menu()
{
	printf("*************************\n");
	printf("*******  1.play   *******\n");
	printf("*******  0.exit   *******\n");
	printf("*************************\n");
}
void game()
	{
	//雷的存储信息
	//1.布置好的雷的信息
	char mind[ROWS][COLS] = { 0 };
	//2.排查出的雷的信息
	char show[ROWS][COLS] = { 0 };
	//初始化
	InitBoard(mind, ROWS, COLS,'0');
	InitBoard(show, ROWS, COLS,'*');
	//打印棋盘
	//DisplayBoard(mind, ROW, COL);
	DisplayBoard(show, ROW, COL);
	//布置雷
	SetMine(mind, ROW, COL);
	DisplayBoard(mind, ROW, COL);
	//扫雷
	FindMIne(mind, show, ROW, COL);
	}

void test()
{
	int input = 0;
	srand((unsigned)time(NULL));
	do
	{
		menu();
		printf("请选择：");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误，重新选择\n");

		}

	} while (input);
}



int main()
{

	test();

	return 0;
}